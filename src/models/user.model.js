import mongoose from 'mongoose'

const userSchema = new mongoose.Schema({
  userName: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
  },
},{
  timestamps: true
})

// User es el [] y userSchema los {} que van dentro del []
export default mongoose.model('User', userSchema)
